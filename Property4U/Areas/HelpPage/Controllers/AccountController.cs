        public IEnumerable<ApplicationUser> GetRandomDevelopers()
        {
            strCurrentUserId = User.Identity.GetUserId();

            List<ApplicationUser> devL = new List<ApplicationUser>();
            // Replace IdentityRole with Custom ApplicationRole - P4U
            var roleManager = new RoleManager<ApplicationRole>(new RoleStore<ApplicationRole>(new ApplicationDbContext()));
            // Return 5 Random Developers base on System Guid
            var devResult = roleManager.FindByName("Developer").Users.OrderBy(d => Guid.NewGuid()).ToList().Take(5);

            foreach(var dev in devResult){
                ApplicationUser user = UserManager.FindById(dev.UserId);
                if (user.FirstName != null && user.LastName != null && user.Id != strCurrentUserId)
                {
                    devL.Add(user);
                }
            }
            return devL.ToList();
        }

